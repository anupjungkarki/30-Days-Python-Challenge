#  Comparision operators are used to compare the two values
# there are various types of comparision operators they are :

# Equal to (==)
x = 5
y = 8
if x == y:
    print(True)
else:
    print(False)

# Not Equal to (!=)
if x != y:
    print(True)
else:
    print(False)

# Greater Than (>)
print(x > y)
# Less Than (<)
print(x < y)
# Greater than or equal to (>=)
print(x >= y)
# Less than or equal to (<=)
print(x <= y)
