# Arithmetic operators: This operators are used to perform simple mathematical and logical operations on numerical values.
# The various operators use in Arithmetic operators are:Addition(+),Subtraction(-),Multiplication(*),Division(/),Modulus(%),Exponent(**) and Floor Division(//)
# Addition
x = 100
y = 30
print(x + y)

#  subtraction
print(x - y)

# Multiplication
print(x * y)

# Division
print(x / y)

# Modulus
print(x % y)

# Exponential
p = 2
n = 4
print(p ** n)

# Floor Division
print(x // y)
