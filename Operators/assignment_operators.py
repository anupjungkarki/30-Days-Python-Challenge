# Assignment Operators in the python
# Assignment operators in python allows assigning value to a variable in a particular manner.
# There are various assignment operators they as follows with the example

#  Assignment (=) : It assign particular value to the variables
x = 3

# Addition assignment (+=): Adds the values specified on the right-hand side operators to the variable
# on the left-hand side of the operator.
x += 3  # which is equivalent to(x = x + 3)
print(x)

# Subtraction assignment (-=): Subtracts the value specified on the right-hand side of the operator from the variable
# on the left-hand side of the operator.
x -= 3  # which is equivalent to (x = x - 3)
print(x)

# Multiplication  assignment(*=) :	Multiplies the variable specified on the left-hand side of
# the operator with the value on the right-hand side of the operator.
x *= 3  # which is equivalent to (x = x * 3)

# Division assignment(/=) :Divides the variable specified on the left-hand side of the operator by
# the value specified on the right-hand side of the operator.
x /= 3  # which is equivalent to ( x = x / 3)

# Modulus assignment (%=): 	Divides the variable on the left-hand side of the operator by the value specified on
# the right-hand side of the operator,
# and places the remainder thus returned by the operation into the variable on the left-hand side
x %= 3  # which to is equivalent (x= x % 3)

# Floor Division assignment(//=):Divides the variable on the left-hand side of the operator by the value specified
# on the right-hand side of the operator, and places the integer part of the result in the variable.
x //= 3  # which to is equivalent (x= x // 3)

# Exponentiation assignment ( **=) :Raises the value of the variable specified on the left-hand side of the operator
# to the power of value specified on the right-hand side of the operator
x **= 3  # which to is equivalent (x= x ** 3)


