# Variable are used to assigned the value in different data types format
# Integer Variable
number1 = 10
number2 = 20
print(type(number1))
sum = number1 + number2
print(sum)

# Float Variable
a = 3.50
b = 4.60
print(type(a))
c = a + b
print(c)

# string variable
f = 'anup'
print(f)
print(type(f))

n = "john"
print(n)

# Boolean Variable create
e = True
print(e)

abcd = 34
gfsf = 45
add = abcd + gfsf
print(add)

# Assigning Multiple variables
x, y, z = "Orange", "apple", "banana"
print(x, y, z)

