# Python set methods
# Add Method
sample_data = {1, 2, 3, 4, 5}
sample_data.add(6)
print(sample_data)

# Remove Method
sample_data.remove(1)
print(sample_data)

# Discard Method
sample_data.discard(5)
print(sample_data)

# Clear Method
sample_data.clear()
print(sample_data)

# Copy Method
sample_data = {1, 2, 3, 4, 5}
copy_data = sample_data.copy()
print(copy_data)

# Pop Method
sample_data = sample_data.pop()
print(sample_data)

# Update Method
x = {1, 2, 3}
x.update([7, 8, 9, 10])
print(x)

