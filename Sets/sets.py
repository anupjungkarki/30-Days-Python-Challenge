# Set are the data type in python that are used to store the collection of data.These data can be a any types.
# It can be integer, string, float or both.We use curly brace for set

# Simple set data
set_data = {1, 2, 3, 4, 5}
print(set_data)

# Access set items
this_set = {'foo', 'bar', 'war'}
for item in this_set:
    print(item)

